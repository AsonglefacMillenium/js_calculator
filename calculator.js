function calculator(a, b) {
    let sum = a + b
    let product = a * b
    let sub = a - b
    let div = a / b


    document.write('<p> The sum is ' +sum+ '</p>')
    document.write('<p> The product is ' +product+ '</p>')
    document.write('<p> The difference is ' +sub+ '</p>')
    document.write('<p> The division is ' +div+ '</p>')
}


calculator(10, 5);